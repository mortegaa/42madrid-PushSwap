#include "../libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new;
	t_list	*first;

	if (!lst || !f)
		return (0);
	if (!(new = ft_lstnew(f(lst->content))))
		return (0);
	first = new;
	while (lst->next)
	{
		lst = lst->next;
		if (!(new->next = ft_lstnew(f(lst->content))))
		{
			ft_lstclear(&first, del);
			return (0);
		}
		new = new->next;
	}
	new->next = 0;
	return (first);
}
