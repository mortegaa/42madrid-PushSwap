#include "libft.h"

int		ft_split_count(char *s, char c)
{
	int		count;

	if (!s)
		return (-1);
	count = !(--s);
	while (*(++s) != '\0')
		if (*s != c && (*(s + 1) == c || *(s + 1) == '\0'))
			count++;
	return (count);
}
